	public function findActiveFormations(): array
	{
		$query = $this->_em->createQuery(
			'SELECT f
			FROM App\Enitiy\Formation f
			WHERE f.dateDebut <= :current AND f.dateFin >= :current
			ORDER BY f.dateDebut DESC'
		)->setParameter('current', date('H:i:s \O\n d/m/Y'));
		return $query->execute();
	}
